let my_array = ['Pizza', 'Hamburger', 'Panini', 'Kebab']

// Ajout fin de tableau
my_array.push('Raclette')

// Ajout début de tableau
my_array.unshift('Hotdog')

// Suppression fin de tableau
my_array.pop()

// Suppression début de tableau
my_array.shift()

// Suppression suivant la position dans le tableau
my_array.splice(2)

// Inserser un tableau
my_array.reverse()

// Connaitre l'index d'un élément
my_array.indexOf('Pizza')